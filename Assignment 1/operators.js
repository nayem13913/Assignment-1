// 1. Declare two variables called "num1" and "num2" and assign them any numeric values.

var num1;
var num2;

num1 = 10;
num2 = 20;


// 2. Use the addition operator to calculate the sum of "num1" and "num2". Store the result in a variable called "sum".

var sum = num1 + num2;
console.log(sum); // 30

// 3. Use the subtraction operator to calculate the difference between "num1" and "num2". Store the result in a variable called "difference".

var difference = num2 - num1;
console.log(difference); // 10


// 4. Use the multiplication operator to calculate the product of "num1" and "num2". Store the result in a variable called "product".
var product = num1 * num2;
console.log(product); // 200


// 5. Use the division operator to calculate the quotient of "num1" divided by "num2". Store the result in a variable called "quotient".
var quotient = num1 / num2;
console.log(quotient); // 0.5


//6. Use the modulus operator to calculate the remainder when "num1" is divided by "num2". Store the result in a variable called "remainder".
var remainder = num2 % num1;
console.log(remainder);


// 7. Print the following information using console.log():

//  - The sum of num1 and num2: "The sum is [sum]."
console.log("The sum is " + sum); // output: The sum is 30


// - The difference between num1 and num2: "The difference is [difference]."
console.log("The difference is " + difference);


//  - The product of num1 and num2: "The product is [product]."
console.log("The product is " + product);


//  - The quotient of num1 divided by num2: "The quotient is [quotient]."
console.log("The quotient is " + quotient);


// - The remainder when num1 is divided by num2: "The remainder is [remainder]."
console.log("The remainder is " + remainder);


